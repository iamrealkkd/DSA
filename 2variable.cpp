#include <iostream>
using namespace std;

int main() {
    // Declare two integer variables
    int num1 = 10; // First number
    int num2 = 20; // Second number

    // Calculate their sum
    int sum = num1 + num2;

    // Display the result
    cout << "The sum of " << num1 << " and " << num2 << " is " << sum << endl;

    return 0;
}
